def count_positives_sum_negatives(arr):
    sum0 = 0
    sum1 = 0
    arr1 = []
    if len(arr) == 0:
        return arr1
    for i in range(len(arr)):
        if arr[i] > 0:
            sum0 += 1
        else:
            sum1 += arr[i]
    arr1.append(sum0)
    arr1.append(sum1)
    return arr1


def count_positives_sum_negatives1(arr):
    if not arr: return []
    pos = 0
    neg = 0
    for x in arr:
        if x > 0:
            pos += 1
        if x < 0:
            neg += x
    return [pos, neg]

