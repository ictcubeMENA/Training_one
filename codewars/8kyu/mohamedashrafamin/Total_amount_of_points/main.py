def points(games):
    res = 0
    for i in games:
        if int(i[0]) == int(i[2]):
            res += 1
        elif int(i[0]) > int(i[2]):
            res += 3
    return res


def points1(games):
    count = 0
    for score in games:
        res = score.split(':')
        if res[0] > res[1]:
            count += 3
        elif res[0] == res[1]:
            count += 1
    return count

