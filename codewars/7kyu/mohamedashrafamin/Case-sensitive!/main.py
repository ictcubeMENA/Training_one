def case_sensitive(s):
    res = []
    if not s:
        res.append(True)
        res.append([])
        return res
    if s.islower():
        res.append(True)
        res.append([])
        return res
    else:
        rr = []
        res.append(False)
        for i in range(len(s)):
            if s[i].isupper():
                rr.append(s[i])
        res.append(rr)
    return res


def case_sensitive1(s):
    return [s.islower() or not s, [c for c in s if c.isupper()]]


