def capitalize(s):
    res = []
    tem = ""
    for i in range(len(s)):
        t = s[i]
        if i % 2 == 0:
            t = s[i].upper()
        tem += t
    res.append(tem)
    tem = ""
    for i in range(len(s)):
        t = s[i]
        if i % 2 != 0:
            t = s[i].upper()
        tem += t
    res.append(tem)
    return res


def capitalize1(s):
    s = ''.join(c if i % 2 else c.upper() for i, c in enumerate(s))
    return [s, s.swapcase()]

