def reverse(a):
    st = ""
    size = []
    res = []
    x = len(a)
    for i in range(x):
        st += a[i]
        if i >= 1:
            size.append(len(a[i]) + size[i - 1])
        else:
            size.append(len(a[i]))
    r = len(st)
    for i in range(x):
        if i >= 1:
            tem = st[r - size[i]:r - size[i - 1]]
            res.append(tem[::-1])
        else:
            tem = st[r - size[i]:r]
            res.append(tem[::-1])

    return res


