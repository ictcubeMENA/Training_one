def remove_duplicate_words(s):
    arr = s.split(" ")
    res = ""
    for i in range(len(arr)):
        if res.find(arr[i]) == -1:
            res += arr[i] + " "
    return res[0:len(res) - 1]


def remove_duplicate_words1(s):
    return ' '.join(dict.fromkeys(s.split()))


