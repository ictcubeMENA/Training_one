def locker_run(lockers):
    res = []
    counter = 1
    for i in range(lockers):
        if i == 0:
            res.append(i + counter)
            counter += 2
        else:
            if res[i - 1] + counter > lockers:
                return res
            res.append(res[i - 1] + counter)
            counter += 2
    return res


